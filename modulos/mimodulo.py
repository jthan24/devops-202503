def suma(a, b):
    """
    Suma dos números enteros.

    Args:
        a (int): Primer número a sumar.
        b (int): Segundo número a sumar.

    Returns:
        int: La suma de los dos números.
    """
    return a + b

def resta(a, b):
    """
    Resta dos números enteros.

    Args:
        a (int): Primer número a restar.
        b (int): Segundo número a restar.

    Returns:
        int: La resta de los dos números.
    """
    return a - b

def multiplicacion(a, b):
    """
    Multiplica dos números enteros.

    Args:
        a (int): Primer número a multiplicar.
        b (int): Segundo número a multiplicar.

    Returns:
        int: El producto de los dos números.
    """
    return a * b

def division(a, b):
    """
    Divide dos números enteros.

    Args:
        a (int): Dividendo.
        b (int): Divisor.

    Returns:
        int: Cociente de la división.
    """
    return a / b
