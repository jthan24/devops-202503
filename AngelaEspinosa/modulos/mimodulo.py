def suma(a, b):
    #Retorna la suma de 2 numeros

    #Parameters:
    #    a (int):Un numero aleatorio.
    #    b (int):Un numero aleatorio.

    #Returns:
    #    suma(int):La suma de los numeros ingresados.   
    return a + b

def resta(a, b):
    #Retorna la resta de 2 numeros

    #Parameters:
    #    a (int):Un numero aleatorio.
    #    b (int):Un numero aleatorio.

    #Returns:
    #    resta(int):La resta de los numeros ingresados.
    return a - b

def multiplicación(a, b):
    #Retorna la multiplicación de 2 numeros

    #Parameters:
    #    a (int):Un numero aleatorio.
    #    b (int):Un numero aleatorio.

    #Returns:
    #    multiplicación(int):La multiplicación de los numeros ingresados.   
    return a * b

def división(a, b):
    #Retorna la división de 2 numeros

    #Parameters:
    #    a (int):Un numero aleatorio.
    #    b (int):Un numero aleatorio.

    #Returns:
    #    división(int):La división de los numeros ingresados.   
    return a / b